// 53. 중앙 이동 알고리즘_2903.swift

let input = Int(readLine()!)!
var mul = 2

for _ in 0..<input {
    mul += mul - 1
}
print(mul*mul)

// 결과 값의 규칙을 분석해보면 n 값이 1 증가할 때 마다 -> mul+mul - 1 을 반복하고, mul*mul 을 출력하면 된다.

//n = 1 ->  2+2-1 = 3
//
//n = 2 ->  3+3-1 = 5*5
//
//n = 3 ->  5+5-1 = 9*9
//
//n = 4 ->  9+9-1 = 17*17
//
//n = 5 ->  17+17-1 = 33*33

